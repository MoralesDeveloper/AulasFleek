function mediaDeNumeros(numero01, numero02, numero03) {
    let media = numero01 + numero02 + numero03
    media = media / 3
    return media
}

console.log(mediaDeNumeros(10, 10, 45))
var media = mediaDeNumeros(10, 20, 18)
var notas = mediaDeNumeros(3, 4, 90)

console.log('Media: ',media)
console.log('Notas: ',notas)
